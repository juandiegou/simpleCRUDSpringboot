package com.example.demo;

/**
 * Enumeración que representa las categorías disponibles para clasificar a los usuarios en el sistema.
 */
public enum EnumCategoria {
    /**
     * Representa la categoría de usuarios relacionada con temas de moda.
     */
    Moda,

    /**
     * Representa la categoría de usuarios relacionada con temas de mercado o compras.
     */
    Mercado,

    /**
     * Representa la categoría de usuarios relacionada con temas de ahorro o finanzas.
     */
    Ahorro,

    /**
     * Representa la categoría de usuarios relacionada con temas de diversión o entretenimiento.
     */
    Diversion
}
